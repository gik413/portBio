<div id="texto"><h3>
        O sistema excretor desempenha um papel crucial na eliminação de resíduos metabólicos, no equilíbrio de fluidos, na regulação do pH do sangue, funções endrócrinas e na ativação
da vitamina D. Ele é responsável por remover produtos tóxicos e excedentes de substâncias do organismo, contribuindo para a saúde geral e a eficiência dos processos corporais.</h3>
