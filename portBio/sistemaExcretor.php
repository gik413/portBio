   <div id="linhas">
    <div id="textoEsquerda"><h3><p>Principais Componentes do Sistema Excretor</p><br>
    Rins: Filtram o sangue para remover resíduos metabólicos, excesso de água e eletrólitos. Eles também regulam a pressão arterial, o pH sanguíneo e a produção de hormônios como a eritropoetina (que estimula a produção de glóbulos vermelhos).<br><br>
    Ureteres: Transportam a urina produzida pelos rins para a bexiga. O movimento da urina pelos ureteres é facilitado por contrações musculares chamadas peristalse.<br><br>
    Bexiga Urinária: Armazena a urina até que seja expelida do corpo. A bexiga se expande à medida que se enche de urina e contrai para expelir a urina durante a micção.<br><br>
    Uretra: Conduz a urina da bexiga para fora do corpo durante a micção. A uretra tem um comprimento e uma estrutura que variam entre homens e mulheres, e possui esfíncteres que ajudam a controlar a liberação da urina.
</h3> </div>
