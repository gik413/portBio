   <div id="texto"><h3><p>Função dos Nêfrons</p><br>
Filtração: A filtração do sangue ocorre no glomérulo, onde o líquido passa para a cápsula de Bowman e forma o filtrado glomerular.<br><br>
Reabsorção: A maior parte da água, sódio, potássio, glicose e outros solutos são reabsorvidos nos túbulos contorcidos proximal e distal e na alça de Henle.<br><br>
Secreção: Certos íons e substâncias são secretados dos capilares peritubulares para os túbulos renais, ajudando a manter o equilíbrio ácido-base e a eliminar resíduos metabólicos. <br><br>
Concentração da Urina: A alça de Henle e os ductos coletores desempenham papéis cruciais na concentração da urina, ajustando a quantidade de água e solutos eliminados pelo corpo.</h3></div>
