    <div id="textoEsquerda"><h3><p>Estrutura e circulação nos néfrons </p><br>
    Glomérulo: É um emaranhado de capilares localizados dentro da cápsula de Bowman. O sangue entra no glomérulo através da arteríola aferente e sai através da arteríola eferente. A pressão nos capilares do glomérulo permite a filtração do plasma sanguíneo para formar um líquido chamado filtrado glomerular.<br><br>

Cápsula de Glomerular: É uma estrutura em forma de copo que envolve o glomérulo.<br><br>

Túbulo Proxima Contorcido(TCP): O filtrado glomerular entra no túbulo contorcido proximal.
O TCP é responsável pela reabsorção significativa de água, sódio, potássio, glicose e outros nutrientes essenciais do filtrado de volta para o sangue.<br><br>

Alça de Henle: Ramo Descendente: Permeável à água, permitindo que a água seja reabsorvida do filtrado para o interstício renal, enquanto os solutos permanecem.
Ramo Ascendente: Impermeável à água, mas reabsorve ativamente sódio, potássio e cloro do filtrado para o interstício renal.<br><br>

Túbulo Distal Contorcido(TCD): O filtrado passa da alça de Henle para o túbulo contorcido distal.
O TCD é responsável pela reabsorção adicional de sódio e secreção de potássio e hidrogênio, ajudando a regular o equilíbrio ácido-base do corpo.<br><br>

Túbulo Coletor: Transportam a urina para os cálices renais e estão envolvidos na regulação final da concentração de urina. São influenciados pelo hormônio antidiurético (ADH), que controla a permeabilidade dos ductos coletore a água, ajustando a concentração final da urina.<br><br>
</h3> </div>
