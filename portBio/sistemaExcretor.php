    <div id="textoDireita"><h3><p>Anatomia dos rins</p><br>
Os rins são dois órgãos localizados atrás da cavidade abdominal, um de cada lado da coluna vertebral.
Cada rim tem cerca de 10 a 12 cm de comprimento, 5 a 7 cm de largura e 3 a 4 cm de espessura.<br><br>
Córtex: <br>
É a camada externa do rim, localizada logo abaixo da cápsula renal.
Contém os corpúsculos renais e os túbulos contorcidos proximais e distais, além de parte dos néfrons. <br><br>

Medula:<br>
Localizada sob o córtex, é composta por pirâmides renais (ou pirâmides de Malpighi) que são estruturas cônicas.
Cada pirâmide possui uma base voltada para o córtex e um ápice, conhecido como papila renal, que se abre na pelve renal.<br><br>

Papila:<br>
As pontas das pirâmides renais que drenam a urina para os cálices renais.

Cálice:<br>
São as pequenas cavidades que coletam a urina das papilas renais.
Existem cálixes maiores (cálix maior) e menores (cálix menor), sendo que os menores drenam para os maiores.<br><br>

Pelve renal:<br>
É a cavidade central onde a urina se acumula antes de ser conduzida para o ureter.
A pelve renal é formada pela junção dos cálixes maiores.<br><br>

Pirâmide renal:<br>
responsáveis por coletar a urina produzida pelos néfrons e conduzi-la para os cálices renais.<br>

Artérias Renais:<br>
Transportam sangue rico em nutrientes e resíduos para os rins.<br><br>

Veias Renais:<br>
Transportam o sangue filtrado dos rins de volta para a circulação sistêmica.<br><br></h3></div>
