    <div id="texto"><h3><p>Intestino delgado</p><br>
    Um longo tubo responsável por finalizar o processo de ingestão e absorver os nutrientes, pode ser separado em 3 partes:<br>
_ _Duodeno: Primeira e menor parte. Começa a alcalinizaçao do quimo (para quebrar quaisquer substâncias que ainda não temham se quebrado)
 *Alcaliniza ele por meio do bicarbonato de cálcio e diminui o ph de 3 para 8 ou 9.<br>
 *Liberação das enzimas: malcase quebra o açúcardos cereais, sacarase quebra o açúcar da cana e lactase quebra o açúcardo leite (intolerantes a lactose tem falhas nessa enzima)<br>
 *Libera os Hormônios secretina, colecistoquinina e peptídeo <br>
 *Recebe suco prancreatico e os sais biliares pelo figado e pâncreas.
<br>
E então quimo vira quilo por virar alcalino.
<br>
   Jejuno:Maior parte no intestino<br> - absorve os nutrientes e substâncias digeridas e excreta as substâncias descartadas, auxiliando no esvaziamento do estômago.
Lamina<br>
    Íleo: Muito parecido com o jejuno
