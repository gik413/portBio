    <div id="textoDireita"><h3><p>Estômago</p>
    no estômago é onde acontece as reações químicas mais fortes, duas partes importântes são:
<br><br>
Cardia: Localizada mais a cima, comunica o órgão com o esôfago.<br>
Piloro: Presente na junção com duodeno, regula a passagem do quimo (como o bolo alimentar é chamdo depois de ser transformado em líquido pastoso)
<br><br>
Como citado, as reações químicas mais fortes acontecerão no estômago, que fará a parte mais importante da digestão ácida.
As celulas do estômago irão secretar as gastrina, que por sua vez irá estimular as células principais para elas então secretarem o ácido clorídrico.
Depois do bolo alimentar passar por isso, ele então será chamado de quino, fica em uma consistência líquida, todos suas proteínas são ingeridas e os restos serão excretados.
