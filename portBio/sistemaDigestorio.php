    <div id="textoDireita"><h3><p>Faringe</p>
    Faringe - também conhecido como a garganta, a faringe faz parte tanto do sistema digestorio como também do respiratório. É divida em 3 partes: Nasofaringe (próxima a cabidade nasal), Orofaringe (próxima a cavidade oral) e a Laringofaringe (abaixo da Orofarimge)<br>
É também ligada aos olhos e nariz, oque explica a razão de quando colocamos algum remédio nesses órgãos, podemos sentir o sabor deles.
<br><br>
  Epliglote- De extrema importância, a epliglote tem a função tanto para respirar quanto para comer, e evitar o engasgo, mas pode acabar falhando, causando o engasgo.</h3></div>
