        <h3>
O sistema digestório, também conhecido como sistema digestivo ou aparelho digestivo, desempenha um papel fundamental em nosso organismo. Sua função é a ingestão dos alimentos, 
ou seja, ele vai receber o alimento e irá quebra-lo até conseguir ingerir os nutrientes presentes ali, e o resto não útil para nosso sistema será descartado nas
fezes.<br>
O sistema digestório é um grande tubo, chamado de tubo digestório, que começa na boca, tendo ela como sua entrada, e acaba no ânus. Os órgão que compõe o tubo em si são:
a boca, faringe, esôfago, estômago, intestino delgado e intestino grosso. Já os outros órgão são chamados de órgãos anexos ou acessórios, eles incluem: o fígado, pâncreas
e as 3 glândulas salivares. <br><br>
Agora vamos explorar sobre a função dos órgãos citados acima: </h3>
