        <h3>
        O sistema respiratório é um dos sistemas vitais do corpo humano, ele é responsável por fazer troca de gases, essencial para a sobrevivência. Sua
 função principal é permitir a entrada de oxigênio no organismo e a eliminação de dióxido de carbono, Processo crucial para o fornecimento de energia necessária para nosso sistema. Além dessa troca, ele também é responsável
pelo olfato e o controle de pH no sangue.<br>
O sistema vai do nariz até abaixo do pulmão e pode ser separado em dua partes: Vias aéreas superiores (inclue os órgãos do pescoço para cima) e Vias aéreas inferiores(inclue os órgãos do pescoço para cima).
</h3>
   </div>
