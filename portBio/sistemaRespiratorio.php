    <div id="texto"><h3><p>Vias aéreas</p><br>
    Como já citado acima, o sistema é composto por duas principais seções de vias aéreas: Vias aéreas superiores e as vias aéreas inferiores. <br> _ _As vias aéreas superiores incluem o nariz,
 a cavidade nasal, os seios paranasais, a faringe e a laringe. Os órgãos acima do pescoço, <br> 
_ _Já vias aéreas inferiores incluem a traqueia, os brônquios e os bronquíolos.<br> Suas funções são: <br><br>
Cavidade nasal: aquece, filtra e umidifica o ar;<br>
Faringe: leva o ar para a laringe;<br>
Laringe: impede a entrada de corpo estranho nas vias aéreas através da epiglote. também é conhecida como o órgão da voz;<br>
Traqueia: tubo formado por 15 a 20 anéis cartilaginosos que leva o ar para os brônquios;<br>
Pulmões: regula os níveis de dióxido de carbono;<br>
Brônquios e Bronquíolos: levam o ar até os os alvéolos pulmonares;<br>
Alvéolos pulmonares: ocorre a troca de gases, do gás carbônico para o rico em oxigênio;<br>
Diafragma: músculo que separa a cavidade torácica da abdominal;.</h3>
