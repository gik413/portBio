<div id="linhas">
    <div id="textoEsquerda"><h3><p>Função das artérias e veias</p><br>
    As artérias são vasos que alimentam os tecidos com oxigênio e nutrientes, enquanto as veias drenam o sangue com o gás carbônico e substâncias produzidas pelo metabolismo das células.<br>
Amabas se dividem em vaso menores para garantir seu acesso a todo o corpo:<br>
• Artérias > Arteríolas > Capilares<br>
• Veias > Vênulas > Capilares<br><br>

Artéria: São vasos sanguíneos que transportam sangue oxigenado do coração para os órgãos e
tecidos do corpo.<br>
• Transportam sangue sob alta pressão<br>
• Sua parede possui elasticidade<br>
São ilustradas com a cor vermelha.<br><br>


Veias: São os vasos sanguíneos que trazem o sangue com menos oxigênio e mais gás carbônico dos órgãos e tecidos ao coração.<br>
As veias possuem válvulas que mantém o sentido
unidirecional do sangue, garantido o seu retorno ao
coração.<br>
São ilustradas com a cor azul.
</h3> </div>
