<div id="texto"><h3>
O sistema circulatório desempenha várias funções cruciais que são essenciais para a manutenção da homeostase e para o funcionamento adequado do corpo humano.<br>
As principais delas sendo: Transporte de Nutrientes e Oxigênio, regulação da Temperatura Corporal, regulação do pH Sanguíneo e remoção de Produtos de Excreção <br>
Seu sistema é dividido em "Sistema cardiovascular" e "Sistema linfático"</h3>
