<div id="linhas">
    <div id="textoEsquerda"><h3><p>Sistema Cardiovascular</p><br>
    Tem como órgão principat o coração, mas poui vasos sanguíneos, artéria e sangue.<br>
  O coração possui 4 cavidades que fazem a circulação do sangue: 1-Átrio direito recebe o sangue desoxigenado e manda para o ventrículo direito;
 2 - Ventrículo direito recebe o sangue e vai se contrair de forma que o sangue será mandado para os pulmôes, onde ele é oxigenado; 3 - Átrio esquerdo recebe o sangue oxigenado, se contrai e o manda para o
Ventrículo esquerdo; 4 - Ventrículo esquerdo - recebe o sangue e o manda para os tecidos do corpo</h3> </div>
