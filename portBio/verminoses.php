    <div id="texto"><h3><p>Quarto exemplo apresentado - Solitária</p><br>
    Também chamada de Teniase, a verminose pode ser causada pelos vermes <i>Taenia solium</i> ou pelo <i>Taenia saginatas</i>.<br>
A doença é causada pela ingestão dos ovos da tenia em carnes, geralmente em carnes crua ou mal cozidas.
Diferente dos outros a <i>Taenia</i> é um verme Plathelmintos e solitário, ou seja, ele é achatado e parasita sozinho, pode chegar a 5 ou 7 metros, mas geralmente se 
apresenta em formas menores chegando a 1 metro. (Imagem 1)
Como citado acima, os vermes causadores podem ser 2, a diferença entre ele é o animal que usam como hospedeiro intermediário : <br>
 <i>Taenia solium</i> É hospedada pelo porco e a <i>Taenia saginatas</i> é hospedada pelo boi. <br>
Seus sintomas incluem dor de cabeça, arritação causada pela fome, fome, dor de cabeça e outros sintomas que podem ser confundidos com um mal-estar. Seu corpo é muito 
logo, como já citado acima, mas ele é segmentado, separado em proglótides ainda imaturas (Imagem 2). Com o tempo o verme pode descartar algumas dessas proglótides, agora maduras.</h3> </div>
  
