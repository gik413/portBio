        <h3>Na primeira aula sobre verminoses foi dada uma pequena introdução para o tema, nesse momento, além da intrudução foram tiradas dúvidas e curiosidades sobre o tema. <br>
        Verminose é qualquer doença causada por organismos considerados vermes, já o nome "verme" é um nome popular dado para 2 grupos de organismos, os Nemathelmintos e Plathelmintos, 
        eles são respectivamente caracterizados por sua forma cilíndrica e sua forma achatada, esses organismos só são considerados em sua forma adulta e podem parasitar em qualquer órgão. <br>
        A infecção acontece pela ingestão dos vermes, ou dos ovos do mesmo, podendo ingeri-los na carne, salada ou outro alimento.
verminoses são mais comumente registradas em crianças pela sua falta de habitos higienicos, mas não só afetam os humanos, como também os animais também, sendo muito comum em cordeiros. 
A melhor forma de previnir a doença é o acessoa ao saneamento básico e habitos de higiente. <br>
