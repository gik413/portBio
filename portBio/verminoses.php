    <div id="textoEsquerda"><h3><p>Primerio exemplo apresentado - Lombriga</p><br>
    Também chamdo de Ascaridiase, a doença é causada pelo agente <i>ascaris Lumbricose</i> que é um nephalteminto.
Esse verme é adquirido por meio oral, ou seja, por meio da ingestão de algum alimento infectado com o verme ou seu ovos que são altamente protegidos (apresentados na foto ao lado). 
O organismo nunca está sozinho e pode se localizar no estômago, pulmões ou otros órgãos, 
seus sintomas incluem inchaço ou dor abdominal, aumento de apetite (Pela falta de minerais que a doença causa) ou a falta de apetite e em alguns caso pode causar manchas na pele.</h3> </div>
