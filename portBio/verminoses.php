    Causada pelo verme <i>oxiúros Enterobius vermicularis</i> .<br>
    O parasita é ingerido, geralmente pela água ou comida infectadas.
    Comos as outras, a verminose é causada pela ingestão dos ovos, muito comum em crianças pela falta de hábiots higiênicos<br>
Seus sintomas incluem coceira e muito desconforto, principlamente pelo verme (imagem ao lado) que é muito pequeno e sai a noite, quando o hospedeiro está dormindo, 
o verme se localiza no reto, próximo ao ânus, e ao se movimentar a noite causa a coceira, levando também à perca de sono do hospedeiro.</h3> </div>
