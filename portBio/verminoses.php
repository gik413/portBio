    <div id="texto"><h3><p>Segundo exemplo apresentado - Barriga d'água</p><br>
    Também chamado de Eschistossomose, é causado pelo verme <i>Schistosoma mansoni</i>. <br>
O verme se infiltra pelo contato da pele, geralmente ao fazer contato com a agua infectada pelas larvas, seu ciclo acontece da seguinte forma:<br>
Os ovos do verme (imagem 1) localizado na água doce irão eclodir e liberar as larvas chamadas "<i>Miracidio</i>" (apresentadas na imagem 2), essas larvas irão infectar os caramujos da água 
e usa-los como hospedeiro intermediário, os mesmos depois de algumas semanas irão liberar as larvas agoras chamadas "<i>Cercárias</i>" que então infectam os humanos. <br>
O organismo tem suas variações de macho e fêmea (apresentados respectivamente nas imagens 3 e 4), geralmente se hospedam no fígado ou intestino.
Seus sintomas incluem inchaço na barriga, causado pelo aumento que ocorre no fígado, problemas de digestão, dor abdonal e outros.</h3> </div>
