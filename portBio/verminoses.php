    <div id="textoEsquerda"><h3><p>Terceiro exemplo apresentado - Amarelão</p>
    A verminose pode ser causada pelos vermes <i>Ancylostoma duodenale</i> e <i>Necator americanus</i>. <br>
    O parasita é ingerido, geralmente pela água ou comida infectadas.
O verme geralmente se hospeda no intestinos, bloqueando o mesmo, a cabeça do organismo possui uma anatomia que o possibilita de se prender nas paredes do intestino (como mostrado na foto ao lado), 
sugando o sangue dali. Seus sintomas incluem anemia, cansaço e indisposição, causados pela falta de minerais que o verme causa. <br>
Um exemplo citado pela professora da representação de um personagem parasitado seria o Jeca Tatu, personagem do filme de mesmo nome.</h3> </div>
