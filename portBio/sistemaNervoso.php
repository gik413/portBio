    <div id="textoDireita"><h3><p>Tipos de Neurônios<p>
    Unipolar:<br>
Possuem um único prolongamento que se divide em duas partes: uma que atua como dendrito e outra como axônio.
Encontrados principalmente em organismos invertebrados. <br>
Em humanos, exemplos incluem os neurônios sensoriais no sistema nervoso periférico.<br><br>


Bipolar:<br>
Tem dois prolongamentos: um dendrito e um axônio, que se estendem a partir do corpo celular.
Estão localizados em áreas específicas como a retina dos olhos, o ouvido interno e o olfato.
 São responsáveis pela transmissão de informações sensoriais.<br><br>

 
Pseudo-unipolar<br>
Parecem ter um único prolongamento que se divide em dois ramos: um que age como dendrito e outro como axônio.
Comuns em gânglios sensoriais do sistema nervoso periférico, como nos gânglios da raiz dorsal.
 São responsáveis pela transmissão de informações sensoriais para o SNC.<br><br>


Multipolar<br>
Possuem um corpo celular com vários dendritos e um único axônio.
O tipo mais comum de neurônio no Sistema Nervoso Central (SNC). Incluem neurônios motores e interneurônios,
 que desempenham papéis em movimentos, reflexos e processamento de informações. 
<br><Br><br>   
</h3> </div>  </div>  
