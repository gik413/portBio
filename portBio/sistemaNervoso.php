<div id="texto"><h3><p>Tecido Nervoso</p>

O tecido nervoso é  responsável pela comunicação entre
 diferentes partes do corpo, processamento 
de informações e controle de funções corporais.
 O tecido nervoso é composto por dois tipos
 principais de células: neurônios e células gliais (ou neuroglia). </h3>
   </div>
   <div id="linhas">
    <div id="textoEsquerda"><h3><p>Neurônios</p><br>
    O tecido nervoso é sensível a vários estímulos que se origina de fora ou do interior do organismo. Ao ser estimulado, esse tecido torna-se capaz de conduzir os impulsos nervosos de maneira rápida e, às vezes, por distâncias relativamente grandes.<br><br>
    Corpo Celular: Contém o núcleo e a maioria dos organelos celulares. É responsável pela produção de proteínas e pela manutenção geral da célula.<br>
    Dendritos: São extensões ramificadas que se projetam a partir do corpo celular. Eles recebem sinais de outros neurônios e conduzem essas informações para o corpo celular.<br>
    Axônio: É uma longa extensão que se projeta a partir do corpo celular e conduz os impulsos nervosos para longe do corpo celular. O axônio pode ser coberto por uma camada de mielina (uma substância lipídica) que aumenta a velocidade de condução dos sinais elétricos.
</h3> </div>
