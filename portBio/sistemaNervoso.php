<div id="texto"><h3>
O sistema nervoso é um dos sistemas mais complexos e vitais do corpo humano, desempenhando funções essenciais na coordenação e 
regulação de quase todas as atividades corporais. Como por exemplo, ele é responsável pelo nosso meio sensorial, tanto sensações internas,
como fome cansaço ou falta de ar, quanto internas, reelacionadas aos 5 sentidos; Recebe a sensação, armazena e gera respota de acordo com o 
que sentimos no momento, alé de estar presente na nossa função motora.</h3>
   </div>

