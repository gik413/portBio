    <div id="textoEsquerda"><h3><p>Células Gliais</p><br>
    Astrócitos: Têm prolongamentos que se conectam a neurônios e vasos sanguíneos, ajudam na sustentação e nutrição.<br>
    Oligodendrócitos e Células de Schwann: Têm extensões que envolvem os axônios, formando camadas de mielina.<br>
    Células ependimárias: Formam uma camada contínua que reveste cavidades no SNC, ajudando na movimentação do líquido cefalorraquidiano. <br>
    Micróglia:  Pequenas e com prolongamentos ramificados que percorrem o tecido nervoso, fagocita detritos celulares e patógenos.<br>
