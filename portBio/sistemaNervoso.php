<div id="texto"><h3> <p>Sistema Nervoso Central e Sistema Nervoso Periférico</p>
O sistema é dividido em "Sistema Nervoso Central (SNC)" e "Sistema Nervoso Periférico (SNP)", os dois apresentados abaixo:<br>
SNC: contém o encéfalo (cérebro, cerebelo, tronco encefálico) e a medula espinhal<br>
SNP: contém nervos cranianos e nervos espinhais</h3>
   </div>
   <br>
<div id="linhas">
    <div id="imagemEsquerda"> <img src="img/morfofisiologia/cerebro.webp" height="350px"> </div>
    <div id="textoDireita"><h3><p>Cérebro<p>
Córtex Cerebral: A camada externa do cérebro, responsável por funções cognitivas superiores, como percepção sensorial, tomada de decisões e controle motor.<br>
Lobo Frontal: Associado ao planejamento, tomada de decisões e controle motor. <br>
Lobo Parietal: Processa informações sensoriais e coordena a percepção espacial.<br>
Lobo Temporal: Envolvido na audição, memória e processamento de linguagem.<br>
Lobo Occipital: Principalmente responsável pela visão.<br>
Cerebelo: Localizado abaixo dos lobos occipitais, é crucial para a coordenação motora, equilíbrio e precisão dos movimentos.<br>
</h3> </div>  </div>   
<br>
<br>  
<div id="linhas">
    <div id="textoEsquerda"><h3><p>Medula Espinhal</p><br>
    Protegido pelas meninges, a medula espinhal tem a função de transmitir sinais entre o cérebro e o resto do corpo. Também realiza alguns reflexos simples, como o reflexo patelar.<br>
    Estrutura: Estende-se desde a base do cérebro até a região lombar da coluna vertebral.<br>
</h3> </div>
