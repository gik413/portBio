    <div id="textoDireita"><h3><p>Potencial de ação<p>
    Refere-se ao impulso elétrico que se propaga ao longo do axônio de um neurônio. Esse processo é crucial para a transmissão de sinais nervosos e a 
comunicação entre neurônios e outras células. Passa pelas fases:<br>

Repouso<br>
A membrana do neurônio está em um estado de repouso com um potencial elétrico de aproximadamente -70 mV  em relação ao exterior da célula.<br><br>


Despolarização<br>
Quando um estímulo suficiente (que atinge o limiar) é aplicado, os canais de sódio voltagem-dependentes se abrem, permitindo a entrada rápida de íons
 Na⁺ para dentro da célula. Isso faz com que o potencial da membrana se torne menos negativo e eventualmente atinja valores positivos (em torno de +30 mV).
 Este é o início do potencial de ação.<br><br>



Repolarização<br>
Após o pico do potencial de ação, os canais de sódio se fecham e os canais de potássio voltagem-dependentes se abrem. Isso permite que os íons K⁺
 saiam da célula. A saída de K⁺ faz com que o potencial da membrana se torne mais negativo novamente, retornando ao estado de repouso.<br><br>


Hiperpolarização<br>
Às vezes, a saída de K⁺ pode fazer com que o potencial da membrana se torne mais negativo do que o potencial de repouso normal, 
criando um estado chamado hiperpolarização. Esse estado é breve, após o qual a membrana retorna ao potencial de repouso.<br><br>



Retorno ao Potencial de Repouso<br>
 A bomba de sódio-potássio (Na⁺/K⁺-ATPase) ajuda a restaurar as concentrações de íons Na⁺ e K⁺ aos seus níveis normais, 
restaurando o potencial de repouso da membrana. O neurônio está pronto para um novo potencial de ação.<br><br>
<br><Br><br>   
</h3> </div>  </div>  


