    <div id="texto"><h3><p>Neurotransmissores</p><br>
    Os neurotransmissores são substâncias químicas usadas para a comunicação entre neurônios e outras células
    do sistema nervoso. Eles são liberados nas sinapses e transmitem sinais elétricos de uma célula para outra. 
    Cada neurotransmissor tem funções específicas e 
    influencia uma variedade de processos fisiológicos e comportamentais. 
