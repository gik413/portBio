    <div id="textoDireita"><h3><p>Nervos Cranianos e Espinhais<p>
    Nervos Cranianos: Saem diretamente do cérebro e são responsáveis por inervar a cabeça e o pescoço. Existem 12 pares separados em 3 classificações diferentes:<br>
    Sensoriais: São responsáveis pela percepção de estímulos sensoriais.<br>
    Motores: Controlam músculos específicos e movimentos.<br>
    Mistos: Têm funções tanto sensoriais quanto motoras.<br><Br>    
    Nervos Espinhais: Saem da medula espinhal e se distribuem por todo o corpo. Existem 31 pares de nervos espinhais, que são divididos em cervical, torácico, lombar, sacral e coccígeo. Tem como funções:<br>
    Sensoriais: Transmitem informações sensoriais do corpo para o SNC, ajudando na percepção de toque, dor, temperatura e posição do corpo.<br>
    Motores: Conduzem comandos motores do SNC para os músculos esqueléticos, permitindo movimentos voluntários.<br>
    Reflexos: Participam de reflexos espinhais, que são respostas automáticas e rápidas a estímulos, como o reflexo patelar.
</h3> </div>  </div>    

