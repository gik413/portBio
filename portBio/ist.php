    <div id="texto"><h3><p>Terceiro exemplo apresentado - HPV</p><br>
    HPV - "Papilomavírus Humano" ou "Vírus do papiloma humano", é um grupo diversificado de vírus que infecta a pele e as mucosas humanas. É considerada uma das ISTs mais prevalentes globalmente.
Muitas vezes é assintomática, ou pode levar meses até manifestar sintomas. <br>
Quando apresentados, os sintomas são :Verrugas genitais(como na foto) e lesões precursoras de câncer, o tratamento vai depender do estado da infecção, mas na 
maior parte dos casos é feito com a cauterização a laser(a queima dessas feridas), mas também pode ser tratado com pomadas, cremes ou ácido tricloroacético para as verrugas,
cirurgia de Alta Frequência e outros.<br>
A maioria das infecções por HPV é eliminada espontaneamente pelo sistema imunológico em até dois anos e podem ser previnidas com a vacinação recomendada para adolescentes antes do início da vida sexual.
</h3> </div>
