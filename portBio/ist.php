    <div id="textoDireita"><h3><p>Quarto exemplo apresentado - Gonorréia</p>
    Causada pela bactéria <i>Neisseria gonorrheae</i>, a gonorréia é outra infecção tratada a partir de antibióticos.<br>
Pode ser assintomática mas quando apresentados, os sintomas podem variar entre homens e mulheres, sendo eles:
Corrimento vaginal ou peniano anormal (geralmente amarelado ou esverdeado), dor ou ardência ao urinar, dor abdominal ou pélvica (especialmente em mulheres), na garganta sensação de queimação e dor, glândulas inchadas, manchas brancas (como na imagem 1) e alterações na fala 

<br><br>
Gonorréia congênita:<br>
No caso de uma mãe infectada, a criança é infectada no parto, o tratamento intensivo tem que ser começado assim que possível, pois a infecção pode afetar de diversas 
formas o desenvolvimento infantil, sendo elas: Conjuntivite neonatal e, por vezes, cegueira e infecção generalizada.</h3> </div>
