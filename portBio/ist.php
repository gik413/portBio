    <div id="texto"><h3><p>Segundo exemplo apresentado - HIV e AIDS</p><br>
    HIV (Vírus da Imunodeficiência Humana)
O HIV é um vírus que ataca o sistema imunológico, especificamente as células linfócitos, que são cruciais para combater infecções. Diferentemente da sífilis, o HIV é um retrovírus ,ou seja, o mesmo tem a capacidade de se integrar no DNA humano (processo mostrado na imagem 1).<br>
Seus sintomas apresentar febre, fadiga, dores musculares ou feridas, grande partes dos sintomas podem ser confundidos por mal-estar, por isso tem casos onde leva anos
para o infectado perceber que stá infectado, além do período em que a infecção não causa sintoma algum, període esse que pode levar de semanas a anos.
A infecção não tem cura, o infectado deverá fazer um acompanhamento médico para controlar a infecção e seus sintomas, sem o tratamento a HIV pode acabar evoluindo para a AIDS.</h3> </div>
