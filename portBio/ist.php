<div id="linhas">
    <div id="texto"><h3><p>AIDS</p><br>
    A AIDS é o estágio mais avançado da infecção pelo HIV. Ela ocorre quando o sistema imunológico foi severamente danificado pelo vírus HIV. AIDS é uma sigla vinda do inglês,
no Brasil a infecção é chamada ded SIDA (Síndrome da Imunodeficiência Adquirida).<br>
Ela é causada pelo HIV não tratado, quando este destrói significativamente as células CD4 do sistema imunológico.<br>
Pode apresentar sintomas como perda de peso severa, febres recorrentes, suores noturnos e fadiga extrema. Também pode trazer algumas infecções oportunistas como: 
Tuberculose, Pneumonia por <i>Pneumocystis jirovecii</i>, Toxoplasmose cerebral e outras.</h3> </div>
